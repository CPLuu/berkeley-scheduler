#!/usr/bin/env bash

TERM='spring-2017'
TERM_ABBRV='sp17'

INPUT_DEPARTMENTS_DIR=intermediate/departments
INPUT_CLASSES_DIR=${INPUT_DEPARTMENTS_DIR}/class-listing-${TERM}-by-subject-area
INPUT_INDICES_DIR=${INPUT_DEPARTMENTS_DIR}/indices

OUTPUT_DEPARTMENTS_DIR=final
OUTPUT_CLASSES_DIR=${OUTPUT_DEPARTMENTS_DIR}/${TERM_ABBRV}/classes
OUTPUT_INDICES_DIR=${OUTPUT_DEPARTMENTS_DIR}/${TERM_ABBRV}/indices

RECOVER_DIR=recover
RECOVER_CLASSES_DIR=${RECOVER_DIR}/classes
RECOVER_INDICES_DIR=${RECOVER_DIR}/indices

mkdir -p ${OUTPUT_DEPARTMENTS_DIR} \
  && cp ${INPUT_DEPARTMENTS_DIR}/departments.json ${OUTPUT_DEPARTMENTS_DIR}

mkdir -p ${OUTPUT_CLASSES_DIR} \
  && mkdir -p ${RECOVER_CLASSES_DIR} \
  && cp ${OUTPUT_CLASSES_DIR}/* ${RECOVER_CLASSES_DIR} \
  && cp ${INPUT_CLASSES_DIR}/* ${OUTPUT_CLASSES_DIR}

mkdir -p ${OUTPUT_INDICES_DIR} \
  && mkdir -p ${RECOVER_INDICES_DIR} \
  && cp ${OUTPUT_INDICES_DIR}/* ${RECOVER_INDICES_DIR} \
  && cp ${INPUT_INDICES_DIR}/* ${OUTPUT_INDICES_DIR}
